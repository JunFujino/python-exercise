#ロジスティック写像のコンピュータ実験
import numpy as np
import matplotlib.pyplot as plt
#=================パラメータ======================
#mu=0.9
#mu=1.5
#mu=2.9
#mu=3.4
mu=4.0
N=100
#=================初期条件========================
x=np.zeros(N+1)
x[0]=0.1
#=================ループ計算======================
for i in range(0,N):
    x[i+1] = mu * x[i] * ( 1 - x[i] )
#=================図を表示する====================
plt.plot(x,'-o')
#plt.plot(x[70:100],'-o')
plt.show()